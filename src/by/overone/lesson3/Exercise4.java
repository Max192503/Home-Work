package by.overone.lesson3;

public class Exercise4 {
    public static void main(String[] args) {
        int a;
        a = 5;
        if (a > 0 && a < 5) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }
        a = 0;
        if (a > 0 && a < 5) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }
        a = -3;
        if (a > 0 && a < 5) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }
        a = 2;
        if (a > 0 && a < 5) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }
    }
}